//Subtraction(-) or difference.

function difference(age1, age2) {
    const diff = age1 - age2;
    console.log(diff); 
}
const fatherAge = 40;
const myAge = 10;

difference(fatherAge, myAge); // এখানে ()  এর মধ্যে fatherAge, myAge ই হলো arguments
difference(50, 10); // এখানে ()  এর মধ্যে 50, 10 ই হলো arguments




